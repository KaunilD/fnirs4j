package org.example;

public class Triple {
    public double hbO, hbR, hbT;

    public double getHbO() {
        return hbO;
    }

    public void setHbO(double hbO) {
        this.hbO = hbO;
    }

    public double getHbR() {
        return hbR;
    }

    public void setHbR(double hbR) {
        this.hbR = hbR;
    }

    public double getHbT() {
        return hbT;
    }

    public void setHbT(double hbT) {
        this.hbT = hbT;
    }
}
